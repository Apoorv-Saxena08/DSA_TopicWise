#include <iostream>
using namespace std;
 
void heapify(int a[], int n, int i) {
	int largest = i;
	int left = 2 * i + 1;
	int right = 2 * i + 2;
	if(left < n && a[left] > a[largest])
    	largest = left;
	if(right < n && a[right] > a[largest])
    	largest = right;
	if(largest != i) {
    	swap(a[i], a[largest]);
    	heapify(a, n, largest);
	}
}
 
void buildHeap(int a[], int n) {
	for(int i = n / 2 - 1; i >= 0; i--)
    	heapify(a, n, i);
}
 
int main() {
	int n;
	cin >> n;
	int a[100];
	for(int i = 0; i < n; i++)
    	cin >> a[i];
	buildHeap(a, n);
	for(int i = 0; i < n; i++)
    	cout << a[i] << " ";
	return 0;
}
